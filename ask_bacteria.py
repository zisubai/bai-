# 第四步：如果都不是，输出“暂时没有这种细菌的信息”
name = input("请输入你想查询的细菌名：")
if name == "大肠杆菌":
    print("大肠杆菌是一种常见的革兰氏阴性菌，广泛存在于人和动物的肠道中。")
elif name == "枯草芽孢杆菌":
    print("枯草芽孢杆菌是一种革兰氏阳性菌，能够形成耐热的芽孢，常用于生物农药和发酵工业。")
else:
    print("暂时没有这种细菌的信息。")
